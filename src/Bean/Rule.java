package Bean;

public class Rule {

	private int id;
	private int t_id;
	private String rule;
	public int getId() {
		return id;
	}
	public int getT_id() {
		return t_id;
	}
	public String getRule() {
		return rule;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setT_id(int t_id) {
		this.t_id = t_id;
	}
	public void setRule(String rule) {
		this.rule = rule;
	}

}
